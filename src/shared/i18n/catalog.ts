export type MessageKey = keyof typeof catalog.zh;

// Compile-time guard: en must have the same keys as zh.
type _AssertSameKeys = keyof typeof catalog.en extends MessageKey
  ? MessageKey extends keyof typeof catalog.en
    ? true
    : never
  : never;
const _keysOk: _AssertSameKeys = true;
void _keysOk;

export function translate(
  locale: Locale,
  key: MessageKey,
  vars?: Record<string, string | number>,
): string {
  const table = catalog[locale] ?? catalog.zh;
  const template = table[key] ?? catalog.zh[key] ?? String(key);
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, name: string) =>
    vars[name] !== undefined ? String(vars[name]) : `{${name}}`,
  );
}
